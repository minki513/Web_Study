package sec01.ex01;

import java.io.IOException;
import java.io.PrintWriter;

import javax.servlet.ServletConfig;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

@WebServlet("/login2")
public class LoginServlet2 extends HttpServlet {
	
	public void init() throws ServletException {
		System.out.println("init 메서드 호출");
	}

	public void destroy() {
		System.out.println("destroy 메서드 호출");
	}

	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		request.setCharacterEncoding("utf-8"); //웹 브라우저에서 전송된 데이터의 인코딩을 설정함
		response.setContentType("text/html;charset=utf-8"); //setContentType()을 이용해 응답할 데이터의 종류가 html임을 설정
		PrintWriter out = response.getWriter();
		String id = request.getParameter("user_id");
		String pw = request.getParameter("user_pw");
		
		String data="<html>";
			data +="<body>";
			data +="아이디 : " + id;
			data +="<br>";
			data +="패스워드 : " + pw;
			data+="</html>";
			data +="</body>";
		out.print(data); //PrintWriter의 print()를 이용해 html태그 문자열을 웹 브라우저로 출력
	}

}
